package it.unibo.model.api;

/**
 * This class is used to manage the direction of the characters in the model.
 */
public enum Direction {
    /**
     * Direction up.
     */
    UP,
    /**
     * Direction right.
     */
    RIGHT,
    /**
     * Direction down.
     */
    DOWN,
    /**
     * Direction left.
     */
    LEFT;
}
